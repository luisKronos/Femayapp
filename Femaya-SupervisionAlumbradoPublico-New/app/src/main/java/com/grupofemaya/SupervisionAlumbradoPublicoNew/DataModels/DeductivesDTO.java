public class DeductivesDTO {

    private String idCheckDeductive;
    private String idCheck;
    private String idDeductive;
    private String createdTime;
    private String punto;

    public String getIdCheckDeductive() {
        return idCheckDeductive;
    }

    public void setIdCheckDeductive(String idCheckDeductive) {
        this.idCheckDeductive = idCheckDeductive;
    }

    public String getIdCheck() {
        return idCheck;
    }

    public void setIdCheck(String idCheck) {
        this.idCheck = idCheck;
    }

    public String getIdDeductive() {
        return idDeductive;
    }

    public void setIdDeductive(String idDeductive) {
        this.idDeductive = idDeductive;
    }

    public String getCreatedTime() {
        return createdTime;
    }

    public void setCreatedTime(String createdTime) {
        this.createdTime = createdTime;
    }

    public String getPunto() {
        return punto;
    }

    public void setPunto(String punto) {
        this.punto = punto;
    }
}
