package com.grupofemaya.SupervisionAlumbradoPublicoNew.DataModels;

public class CarDTO {

    private String placas;
    private String torretas;
    private String luces;
    private String laminas;
    private String lona;
    private String imagen;

    public String getPlacas() {
        return placas;
    }

    public void setPlacas(String placas) {
        this.placas = placas;
    }

    public String getTorretas() {
        return torretas;
    }

    public void setTorretas(String torretas) {
        this.torretas = torretas;
    }

    public String getLuces() {
        return luces;
    }

    public void setLuces(String luces) {
        this.luces = luces;
    }

    public String getLaminas() {
        return laminas;
    }

    public void setLaminas(String laminas) {
        this.laminas = laminas;
    }

    public String getLona() {
        return lona;
    }

    public void setLona(String lona) {
        this.lona = lona;
    }

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }
}
